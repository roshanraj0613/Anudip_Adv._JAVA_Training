package CollectionsExample;
import java.util.ArrayList;


public class Student {
    private int stdId;
    private String stdName;
    private int age;

    public Student(int stdId, String stdName, int age) {
        this.stdId = stdId;
        this.stdName = stdName;
        this.age = age;
    }

    public int getStdId() {
        return stdId;
    }


}
